// Logger defines a set of methods for writing application logs. Derived from and
// inspired by logrus.Entry.
type Logger interface {
	Debug(args ...interface{})
	Debugf(format string, args ...interface{})
	Debugln(args ...interface{})
	Error(args ...interface{})
	Errorf(format string, args ...interface{})
	Errorln(args ...interface{})
	Fatal(args ...interface{})
	Fatalf(format string, args ...interface{})
	Fatalln(args ...interface{})
	Info(args ...interface{})
	Infof(format string, args ...interface{})
	Infoln(args ...interface{})
	Panic(args ...interface{})
	Panicf(format string, args ...interface{})
	Panicln(args ...interface{})
	Print(args ...interface{})
	Printf(format string, args ...interface{})
	Println(args ...interface{})
	Warn(args ...interface{})
	Warnf(format string, args ...interface{})
	Warning(args ...interface{})
	Warningf(format string, args ...interface{})
	Warningln(args ...interface{})
	Warnln(args ...interface{})
}

var defaultLogger *logrus.Logger

type baseLogger struct {
	*logrus.Logger
}

func (f *baseLogger) Log(args ...interface{}) error {
	f.Info(args...)
	return nil
}

// MiddlewareLogger logs all requests
var MiddlewareLogger log.Logger

func init() {
	defaultLogger = defaultLogrusLogger()
	{
		MiddlewareLogger = &baseLogger{defaultLogger}
		MiddlewareLogger = log.With(MiddlewareLogger, "ts", log.DefaultTimestampUTC)
		MiddlewareLogger = log.With(MiddlewareLogger, "caller", log.DefaultCaller)
	}
}

// LoggerMiddleware returns an endpoint middleware that logs the
// duration of each invocation, and the resulting error, if any.
func LoggingMiddleware(logger log.Logger) endpoint.Middleware {

	return func(next endpoint.Endpoint) endpoint.Endpoint {
		return func(ctx context.Context, request interface{}) (response interface{}, err error) {
			defer func(begin time.Time) {
				_ = logger.Log("error", err, "request took", time.Since(begin))
			}(time.Now())

			return next(ctx, request)
		}
	}
}

func defaultLogrusLogger() *logrus.Logger {
	return newLogrusLogger("", false)
}

func newLogrusLogger(logLevel string, formatJson bool) *logrus.Logger {

	l := logrus.New()

	if formatJson {
		l.Formatter = new(logrus.JSONFormatter)
	}
	l.Out = os.Stderr

	switch logLevel {
	case "debug":
		l.Level = logrus.DebugLevel
	case "warning":
		l.Level = logrus.WarnLevel
	case "info":
		l.Level = logrus.InfoLevel
	default:
		l.Level = logrus.DebugLevel
	}

	return l
}

// Fields enumerates logger fields
type Fields map[string]interface{}

// With adds field
func (f Fields) With(k string, v interface{}) Fields {
	f[k] = v
	return f
}

// WithFields adds relevant fields to the log entry
func (f Fields) WithFields(f2 Fields) Fields {
	for k, v := range f2 {
		f[k] = v
	}
	return f
}

// WithFields adds relevant fields to the log entry
func WithFields(fields Fields) Logger {
	return defaultLogger.WithFields(logrus.Fields(fields))
}

